const fruta = "manga";
switch (fruta) {
    case "maca":
        console.log("A fruta escolhida foi a maca");
        break;
    case "banana":
        console.log("A fruta escolhida foi a banana");
        break;
    case "laranja":
        console.log("A fruta escolhida foi a laranja");
        break;
    case "manga":
        console.log("A fruta escolhida foi a manga");
        break;
    default:
        console.log("Nenhuma fruta foi escolhida");
        break;
}